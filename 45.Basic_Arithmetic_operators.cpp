#include <iostream>
using namespace std;
int main()
{
    int a = 11;
    int b = 5;
    int c;

    cout << "a + b is :" << a + b << endl; // 11+5

    cout << "a - b is :" << a - b << endl; // 11-5

    cout << "a * b is :" << a * b << endl; // 11*5

    cout << "a / b is :" << a / b << endl; // 11/5

    cout << "a % b is :" << a % b << endl; // 11%5

    cout << "a++ is :" << a++ << endl; // 11++

    cout << "a-- is :" << a-- << endl; // 12--

    return 0;
}